# length operation
len([1, 2, 3]) == 3

# repetition
['Hello'] * 3 == ['Hello', 'Hello', 'Hello']

# concatenation
[1, 2, 3] + [4, 5, 6] == [1, 2, 3, 4, 5, 6]

# merges two lists, the membership operation, which checks for the presence
# of a particular item in the array
'butter' in ['bread', 'butter', 'cheese'] == True
